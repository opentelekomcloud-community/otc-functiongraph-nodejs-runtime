const https = require("https");
const { Signer, HttpRequest } = require("otc-api-sign-sdk-nodejs");
const path = require("path");

const fs = require("fs");
const { type } = require("os");

const region = process.env.OTC_SDK_REGION || "eu-de";

const fgEndpoint = `https://functiongraph.${region}.otc.t-systems.com`;
const projectId = process.env.OTC_SDK_PROJECT_ID;
const ak = process.env.OTC_SDK_AK;
const sk = process.env.OTC_SDK_SK;

const functionName = "getNodeJSRuntimeInfo";
const functionVersion = "latest";
const functionApp = "default";

const OUTPUT_FOLDER = path.join(__dirname, "../../../doc/source/devguide/bundled_libraries/");

async function getLibs(runtime) {
  const functionURN = `urn:fss:${region}:${projectId}:function:${functionApp}:${functionName}:${functionVersion}`;

  // Endpoint for synchronous invocation
  const invokeURI = `${fgEndpoint}/v2/${projectId}/fgs/functions/${functionURN}/invocations`;

  const RUNTIME = runtime;
  // set body according to your function input
  const body = {
    runtime: RUNTIME,
  };

  // set request method
  const method = "POST";

  // set headers
  const headers = {
    "Content-Type": "application/json;charset=utf8",
    Host: new URL(fgEndpoint).host,
    "X-Cff-Log-Type": "",
    "X-CFF-Request-Version": "v1",
    "X-Project-Id": projectId,
  };

  // create HttpRequest instance
  const request = new HttpRequest(
    method,
    invokeURI,
    headers,
    JSON.stringify(body),
  );

  // create Signer instance and set AK/SK
  const signer = new Signer();
  signer.Key = ak;
  signer.Secret = sk;

  // sign the request
  const signedRequest = signer.Sign(request);

  return new Promise((resolve, reject) => {
    // send the signed request
    const req = https.request(signedRequest, function (res) {
      res.setEncoding("utf8");

      let data = "";
      res.on("data", function (chunk) {
        data += chunk.toString();
      });

      res.on("end", async function () {
        try {
          const result = JSON.parse(data).result;
          const libs = JSON.parse(result);

          resolve(libs);
        } catch (error) {
          reject(error);
        }
      });
    });

    req.on("error", function (e) {
      console.error("Error: ", e);
      reject(e);
    });

    req.write(JSON.stringify(body));
    req.end();
  });
}

async function writeIndexFile(runtimes) {
  const docPath = OUTPUT_FOLDER;

  return new Promise((resolve, reject) => {
    const stream = fs.createWriteStream(path.join(docPath, "_index.rst"));

    stream.on("error", reject);
    stream.on("finish", resolve);

    stream.write(
      "..\n   _This file is auto-generated by createLibraryDocs.js. Do not edit it directly.\n\n",
    );

    stream.write("Bundled Libraries\n");
    stream.write("========================\n\n");

    stream.write(
      "Bundled libraries can be used for **event** and **http** functions **build from scratch**.\n",
    );

    stream.write(
      "\nFor runtimes bundled with a NodeJS runtime version see following pages:\n",
    );

    stream.write("\n.. toctree::\n");
    stream.write("   :maxdepth: 1\n\n");

    for (const runtime of runtimes) {
      stream.write(`    ${runtime} <bundled_libraries_${runtime}>\n`);
    }

    stream.end();
  });
}

async function writeFile(runtime, libs) {
  const docPath = OUTPUT_FOLDER;

  return new Promise((resolve, reject) => {
    const stream = fs.createWriteStream(
      path.join(docPath, "bundled_libraries_" + runtime + ".rst"),
    );

    stream.on("error", reject);
    stream.on("finish", resolve);

    stream.write(
      `..\n   _This file is auto-generated by createLibraryDocs.js. Do not edit it directly.\n\n`,
    );

    stream.write(
      `Bundled third-party components in the Node.js ${runtime.replace("nodejs", "")}\n`,
    );
    stream.write(
      "=================================================================\n",
    );
    stream.write("\n");

    if (Array.isArray(libs)) {
      stream.write(
        "Following list shows the bundled third-party components integrated in the Node.js runtime for FunctionGraph. \n",
      );
      stream.write("\n");

      stream.write(
        `.. list-table:: Bundled Libraries in Node.js Runtime ${runtime}\n`,
      );
      stream.write("    :header-rows: 1\n");
      stream.write("\n");
      stream.write("    * - Name\n");
      stream.write("      - Version\n");
      stream.write("      - License\n");
      stream.write("\n");

      for (const pkg of libs) {
        if (pkg.homepage) {
          stream.write(`    * - \`${pkg.name} <${pkg.homepage}>\`_\n`);
        } else {
          stream.write(`    * - ${pkg.name}\n`);
        }
        stream.write(`      - ${pkg.version}\n`);
        stream.write(`      - ${pkg.license}\n`);
        stream.write("\n");
      }
    } else {
      stream.write("No library information available for this runtime.\n");
    }

    stream.end();
  });
}

async function main() {
  if (!fs.existsSync(OUTPUT_FOLDER)) {
    console.error(`Output folder not found: ${OUTPUT_FOLDER}`);
    return;
  }
  try {
    const runtimes = await getLibs("");
    console.log("Runtimes: ", runtimes);

    await writeIndexFile(
      runtimes.sort(function (a, b) {
        // Semver sorting
        const a1 = a.replace("nodejs", "").split(".");
        const b1 = b.replace("nodejs", "").split(".");
        const len = Math.min(a1.length, b1.length);

        for (let i = 0; i < len; i++) {
          const a2 = +a1[i] || 0;
          const b2 = +b1[i] || 0;

          if (a2 !== b2) {
            return a2 > b2 ? -1 : 1;
          }
        }

        return b1.length - a1.length;
      }),
    );

    for (const runtime of runtimes) {
      const libs = await getLibs(runtime);
      console.log(`Libraries for runtime ${runtime}: `);

      await writeFile(runtime, libs);
    }
  } catch (error) {
    console.error("Failed to get runtimes:", error);
  }
}

main();
